<template>
  <div class="login_container">
    <div class="title"><b>泥&nbsp;&nbsp;石&nbsp;&nbsp;流&nbsp;&nbsp;监&nbsp;&nbsp;测&nbsp;&nbsp;预&nbsp;&nbsp;警&nbsp;&nbsp;系&nbsp;&nbsp;统</b></div>
    <div class="login_box">
    <!--登陆表单-->
    <el-form ref="loginFormRef" :model="loginForm" :rules="loginFormRules" label-width="0px" class="login_form">
<!--      用户名-->
      <el-form-item prop="username">
        <el-input v-model="loginForm.username" prefix-icon="iconfont icon-user"></el-input>
      </el-form-item>
<!--      密码-->
      <el-form-item prop="pwd">
        <el-input type="password" v-model="loginForm.pwd" prefix-icon="iconfont icon-3702mima"></el-input>
      </el-form-item>
<!--      按钮-->
      <el-form-item class="btn">
        <el-button type="primary" @click="login">登录</el-button>
        <el-button type="info" @click="resetLoginForm">重置</el-button>
      </el-form-item>
    </el-form>
    </div>
  </div>
</template>

<script>
import qs from 'qs'

export default {
  data () {
    return {
      // 这是登录表单的数据绑定对象
      loginForm: {
        username: '',
        pwd: ''
      },
      // 这是表单的验证规则对象
      loginFormRules: {
        // 验证用户名是否合法
        username: [
          {required: true, message: '请输入登录名称', trigger: 'blur'},
          {min: 3, max: 10, message: '长度在3到10个字符', trigger: 'blur'}
        ],
        // 验证密码是否合法
        pwd: [
          {required: true, message: '请输入登录密码', trigger: 'blur'},
          {min: 5, max: 15, message: '长度在5到15个字符', trigger: 'blur'}
        ]
      }
    }
  },
  methods: {
    // 点击重置按钮，重置登录表单
    resetLoginForm () {
      // console.log(this)
      this.$refs.loginFormRef.resetFields()
    },
    login () {
      this.$refs.loginFormRef.validate(async valid => {
        if (!valid) return
        const {data: res} = await this.$http.post('/User/logIn', qs.stringify(this.loginForm))
        if (res.success !== true) return this.$message.error('登录失败')
        this.$message.success('登陆成功')
        // 1.将登录成功之后的token，保存到客户端的sessionStorage中
        // 1.1项目中除了登录之外的其他API接口，必须在登录之后才能访问
        // 1.2token只应在当前网站打开期间生效，所以将token保存在sessionStorage中
        // window.sessionStorage.setItem('token', res.data.token)
        // 2.通过编程式导航跳转到后台主页，路由地址是/home
        await this.$router.push('/home')
      })
    }
  }
}
</script>

